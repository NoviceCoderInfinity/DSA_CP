class Solution{
    public:
    vector<long long int> twoOddNum(long long int Arr[], long long int N)  
    {
        // code here
        set<long long int> elements;
        for (long long int i = 0; i < N; ++i) {
            long long int num = Arr[i];
            if (elements.find(num) == elements.end()) {elements.insert(num);}
            else {elements.erase(num);}
        }
        vector<long long int> v(elements.begin(), elements.end());
        sort(v.rbegin(), v.rend());
        return v;
    }
};

